# ตัวดำเนินการ & ตัวที่ถูกดำเนินการ
# Operator | Operand
# A + B
# A , B => Operand
# + => Operator

x = 5
y = 10
print("บวก = : " , x + y)
print("ลบ = : ", x - y)
print("คูณ = : ", x * y)
print("หาร = : " , x / y)
print("หารปัดเศษ = : ", x // y)
print("ยกกำลัง = : ", x**y)
print("หารเอาเศษ = : ", x%y)